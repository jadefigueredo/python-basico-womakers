x = int(input('Digite um número inteiro: '))
y = int(input('Digite outro número inteiro: '))

soma = x + y
subtracao = x - y
multiplicacao = x * y
divisao = x / y
print(f'{x}+{y} = {soma:.2f}')
print(f'{x}-{y} = {subtracao:.2f}')
print(f'{x}*{y} = {multiplicacao:.2f}')
print(f'{x}/{y} = {divisao:.2f}')